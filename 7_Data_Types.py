# String data type
x = str("Hello World")

# Integer data type
x = int(20)

# Float data type
x = float(20.5)

# Complex number data type
x = complex(1j)

# List data type
x = list(("apple", "banana", "cherry"))

# Tuple data type
x = tuple(("apple", "banana", "cherry"))

# Range data type
x = range(6)

# Dictionary data type
x = dict(name="John", age=36)

# Set data type
x = set(("apple", "banana", "cherry"))

# Frozenset data type
x = frozenset(("apple", "banana", "cherry"))

# Boolean data type
x = bool(5)

# Bytes data type
x = bytes(5)

# Bytearray data type
x = bytearray(5)

# Memoryview data type
x = memoryview(bytes(5))
